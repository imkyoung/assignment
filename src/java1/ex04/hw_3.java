package java1.ex04;

public class hw_3 {
    public static void main(String[] args) {
        int num1=15, num2=7;

        if(num1%3==0){
            System.out.println("num1을 3으로 나눈 나머지가 0입니다.");
        }
        else{
            System.out.println("num1을 3으로 나눈 나머지가 0이 아닙니다.");
        }

        if(num2%4==0){
            System.out.println("num2을 4으로 나눈 나머지가 0입니다.");
        }
        else{
            System.out.println("num2을 3으로 나눈 나머지가 0이 아닙니다.");
        }
    }
}
